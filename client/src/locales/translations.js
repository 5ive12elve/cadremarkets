// Helper function to get translation
export const useTranslation = (page, key, language = 'en') => {
  try {
    return translations[page][language][key] || translations[page]['en'][key] || key;
  } catch {
    console.warn(`Translation not found: ${page}.${key}`);
    return key;
  }
};

// Helper function to get all translations for a page
export const getPageTranslations = (page, language = 'en') => {
  try {
    return translations[page][language] || translations[page]['en'] || {};
  } catch {
    console.warn(`Page translations not found: ${page}`);
    return {};
  }
}; 